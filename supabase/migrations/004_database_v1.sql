alter table public.festivals
add column if not exists address text,
add column if not exists region text,
add column if not exists official_url text,
add column if not exists status text default 'scheduled',
add column if not exists thumbnail_url text,
add column if not exists price_type text,
add column if not exists slug text;

alter table public.festivals
alter column start_date set not null,
alter column end_date set not null;

alter table public.festivals
drop constraint if exists festivals_valid_date_range;

alter table public.festivals
add constraint festivals_valid_date_range
check (
  end_date >= start_date
);

alter table public.festivals
drop constraint if exists festivals_valid_category;

alter table public.festivals
add constraint festivals_valid_category
check (
  category is null
  or category in (
    'music_festival',
    'local_festival',
    'food_festival',
    'culture_festival',
    'other'
  )
);

alter table public.festivals
drop constraint if exists festivals_valid_verification_status;

alter table public.festivals
add constraint festivals_valid_verification_status
check (
  verification_status is null
  or verification_status in (
    'pending',
    'approved',
    'rejected'
  )
);

alter table public.festivals
drop constraint if exists festivals_valid_status;

alter table public.festivals
add constraint festivals_valid_status
check (
  status is null
  or status in (
    'scheduled',
    'ongoing',
    'ended',
    'cancelled'
  )
);

alter table public.festivals
drop constraint if exists festivals_valid_price_type;

alter table public.festivals
add constraint festivals_valid_price_type
check (
  price_type is null
  or price_type in (
    'free',
    'paid',
    'partial_free',
    'unknown'
  )
);

create unique index if not exists festivals_slug_unique
on public.festivals (slug)
where slug is not null;

create unique index if not exists festivals_name_start_location_unique
on public.festivals (
  lower(trim(name)),
  start_date,
  lower(trim(coalesce(location, '')))
);

create index if not exists idx_festivals_region
on public.festivals (region);

create index if not exists idx_festivals_status
on public.festivals (status);

create index if not exists idx_festivals_verification_status
on public.festivals (verification_status);

create index if not exists idx_festivals_date_range
on public.festivals (start_date, end_date);

