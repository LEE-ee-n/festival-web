-- =========================================================
-- 4. festival_candidates 생성
-- =========================================================

create table if not exists public.festival_candidates (
  id bigint generated by default as identity primary key,

  title text not null,
  source_url text not null,
  source_type text,

  raw_text text,

  festival_name text,
  start_date date,
  end_date date,
  location text,
  category text,

  score integer default 0,
  status text not null default 'pending',

  reject_reason text,
  reviewed_at timestamptz,
  created_at timestamptz default now(),
  updated_at timestamptz default now(),

  festival_id bigint,

  constraint festival_candidates_source_url_unique
    unique (source_url),

  constraint festival_candidates_valid_date_range
    check (
      start_date is null
      or end_date is null
      or end_date >= start_date
    ),

  constraint festival_candidates_valid_category
    check (
      category is null
      or category in (
        'music_festival',
        'local_festival',
        'food_festival',
        'culture_festival',
        'other'
      )
    ),

  constraint festival_candidates_valid_status
    check (
      status in (
        'pending',
        'approved',
        'rejected'
      )
    )
);

alter table public.festival_candidates
drop constraint if exists festival_candidates_festival_id_fkey;

alter table public.festival_candidates
add constraint festival_candidates_festival_id_fkey
foreign key (festival_id)
references public.festivals(id)
on delete set null;

create index if not exists idx_festival_candidates_status
on public.festival_candidates (status);

create index if not exists idx_festival_candidates_score
on public.festival_candidates (score);

create index if not exists idx_festival_candidates_created_at
on public.festival_candidates (created_at);

create index if not exists idx_festival_candidates_start_date
on public.festival_candidates (start_date);

-- =========================================================
-- 5. updated_at 자동 갱신
-- =========================================================

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists set_festivals_updated_at
on public.festivals;

create trigger set_festivals_updated_at
before update on public.festivals
for each row
execute function public.set_updated_at();

drop trigger if exists set_festival_candidates_updated_at
on public.festival_candidates;

create trigger set_festival_candidates_updated_at
before update on public.festival_candidates
for each row
execute function public.set_updated_at();

